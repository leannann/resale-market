package ru.skypro.homework.dto;

import io.swagger.v3.oas.annotations.media.Schema;

/**
 * Перечисление возможных ролей пользователя в системе.
 * <p>
 * Роль определяет уровень доступа к операциям:
 * <ul>
 *     <li>{@link #USER} — обычный пользователь, может создавать объявления,
 *         оставлять комментарии и управлять только своим контентом;</li>
 *     <li>{@link #ADMIN} — администратор, имеет расширенные права,
 *         включая возможность управлять объявлениями и комментариями других пользователей.</li>
 * </ul>
 */
@Schema(description = "Роль пользователя")
public enum Role {

    /** Обычный пользователь системы. */
    USER,

    /** Администратор с расширенными правами. */
    ADMIN
}

